<script>
import http from '../../axios';

export default {
    name: 'Table',
    props: {
        path: String,
    },
    data() {
        return {
            columns: [],
            data: [],
            load: true,
            popup: false,
            loading: false,
        };
    },
    methods : {
        async fetchData() {
            try {
                this.load = true;
                const response = await http.get(this.path);
                this.columns = Object.keys(response.data["hydra:member"][0]);
                this.data = response.data["hydra:member"];
            } finally {
                this.load = false;
            }
        },
        async _handleDelete(id) {
            try {
                this.loading = true;
                await http.delete(this.path + '/' + id);
            } finally {
                this.popup = false;
                this.loading = false;
                this.fetchData();
            }
        },
        async _handleEdit(id) {
            this.$router.push({ 
                name: 'admin_form_edit',
                params: { id: id },
                query: {
                    submitApi: this.path,
                    submitApiMethod: 'put',
                }});
        },
    },
};
